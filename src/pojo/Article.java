package pojo;

public class Article {
    private String artId;
    private String artTitle;
    private String artText;
    private String artDate;
    private String artWriter;

    public Article() {
    }

    public String getArtId() {
        return artId;
    }

    public void setArtId(String artId) {
        this.artId = artId;
    }

    public String getArtTitle() {
        return artTitle;
    }

    public void setArtTitle(String artTitle) {
        this.artTitle = artTitle;
    }

    public String getArtText() {
        return artText;
    }

    public void setArtText(String artText) {
        this.artText = artText;
    }

    public String getArtDate() {
        return artDate;
    }

    public void setArtDate(String artDate) {
        this.artDate = artDate;
    }

    public String getArtWriter() {
        return artWriter;
    }

    public void setArtWriter(String artWriter) {
        this.artWriter = artWriter;
    }
}
